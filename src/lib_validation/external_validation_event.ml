(** Declares logging events for external_validation_event *)

include Internal_event.Simple

let section = ["external_validation"]

let socket_path_too_long_event =
  declare_1
    ~section
    ~name:"socket_path_too_long"
    ~msg:
      "The socket path {path} is too long. Please set an alternative path \
       using XDG_RUNTIME_DIR to specify where to create the file"
    ~level:Fatal
    ("path", Data_encoding.string)
