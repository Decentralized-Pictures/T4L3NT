type t = int32

let t = Repr.int32

let equal x y = Compare.Int32.equal x y

let hash = Hashtbl.hash

let hash_size = 30

let encoded_size = 4

let encode v =
  let b = Bytes.create encoded_size in
  TzEndian.set_int32 b 0 v ;
  Bytes.unsafe_to_string b

let decode str i = TzEndian.get_int32_string str i

let pp fmt i = Format.fprintf fmt "%ld" i
