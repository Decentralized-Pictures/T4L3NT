open Lang_core
open Lang_stdlib

module type P_HASH = sig
  type scalar = S.t

  type ctxt

  val init : ?input_length:int -> unit -> ctxt

  val digest : ctxt -> scalar array -> ctxt

  val get : ctxt -> scalar

  val direct : ?input_length:int -> scalar array -> scalar
end

module type HASH = functor (L : LIB) -> sig
  open L

  val digest : ?input_length:int -> scalar list repr -> scalar repr t
end
