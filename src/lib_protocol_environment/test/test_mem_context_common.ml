let fold_keys s root ~init ~f =
  Context.fold s root ~order:`Sorted ~init ~f:(fun k v acc ->
      match Context.Tree.kind v with
      | `Value -> f (root @ k) acc
      | `Tree -> Lwt.return acc)

let keys t = fold_keys t ~init:[] ~f:(fun k acc -> Lwt.return (k :: acc))

let domain ctxt = keys ctxt []
